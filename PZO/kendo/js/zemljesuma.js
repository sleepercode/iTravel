window.onload = function () {
    var geopokrice = document.getElementById("geopokrice"),
        destinacije = document.getElementById("destinacije"),
        osigsuma = document.getElementById("osigsuma");
    for (var state in stateObject) {
        geopokrice.options[geopokrice.options.length] = new Option(state, state);
    }
    geopokrice.onchange = function () {
        destinacije.length = 1; // remove all options bar first
        osigsuma.length = 1; // remove all options bar first
        if (this.selectedIndex < 1) return; // done   
        for (var county in stateObject[this.value]) {
            destinacije.options[destinacije.options.length] = new Option(county, county);
        }
    }
    geopokrice.onchange(); // reset in case page is reloaded
    destinacije.onchange = function () {
        osigsuma.length = 1; // remove all options bar first
        if (this.selectedIndex < 1) return; // done   
        var cities = stateObject[geopokrice.value][this.value];
        for (var i = 0; i < cities.length; i++) {
            osigsuma.options[osigsuma.options.length] = new Option(cities[i], cities[i]);
        }
    }
}
